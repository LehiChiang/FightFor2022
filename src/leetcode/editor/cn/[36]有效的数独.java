class isValidSudokuSolution {
    public boolean isValidSudoku(char[][] board) {
        boolean isValid = true;
        int[] flag = new int[9];
        for (char[] row : board) {
            for (char c : row) {
                if (c != '.') {
                    if (flag[c - 49] == 1)
                        return false;
                    else
                        flag[c - 49] = 1;
                }
                else continue;
            }
            Arrays.fill(flag, 0);
        }

        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                if (board[j][i] != '.') {
                    if (flag[board[j][i] - 49] == 1)
                        return false;
                    else
                        flag[board[j][i] - 49] = 1;
                }
                else continue;
            }
            Arrays.fill(flag, 0);
        }

        for (int i = 0; i < board.length; i+=3)
            for (int j = 0; j < board[0].length; j+=3) {
                for (int ii = 0; ii < 3; ii++)
                    for (int jj = 0; jj < 3; jj++) {
                        if (board[i+ii][j+jj] != '.') {
                            if (flag[board[i+ii][j+jj] - 49] == 1)
                                return false;
                            else
                                flag[board[i+ii][j+jj] - 49] = 1;
                        }
                        else continue;
                    }
            }
        return isValid;
    }

    public static void main(String[] args) {
        isValidSudokuSolution solution = new isValidSudokuSolution();
        System.out.println(solution.isValidSudoku(new char[][]{
                {'5', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        }));
    }
}
